#include <stdio.h>

int main() {
    int a = 20, b = 6;

    printf("a = %d, b = %d\n", a, b);
    printf("Addition (a + b) = %d\n", a + b);
    printf("Subtraction (a - b) = %d\n", a - b);
    printf("Multiplication (a * b) = %d\n", a * b);
    printf("Division (a / b) = %d\n", a / b);   // integer division
    printf("Modulus (a %% b) = %d\n", a % b);  // remainder

    return 0;
}
